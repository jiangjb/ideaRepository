public class AmCinema {

    private String id;
    private int vote;
    private int sort;
    private int isdisplay;
    private String cityid;
    private String name;
    private String addr;
    
    public String getId() {
        return id;
    }
    
    public void setId(String id) {
        this.id = id;
    }
    
    public int getVote() {
        return vote;
    }
    
    public void setVote(int vote) {
        this.vote = vote;
    }
    
    public int getSort() {
        return sort;
    }
    
    public void setSort(int sort) {
        this.sort = sort;
    }
    
    public int getIsdisplay() {
        return isdisplay;
    }
    
    public void setIsdisplay(int isdisplay) {
        this.isdisplay = isdisplay;
    }
    
    public String getCityid() {
        return cityid;
    }
    
    public void setCityid(String cityid) {
        this.cityid = cityid;
    }
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public String getAddr() {
        return addr;
    }
    
    public void setAddr(String addr) {
        this.addr = addr;
    }

    @Override
    public String toString() {
        return "AmCinema [id=" + id + ", vote=" + vote + ", sort=" + sort + ", isdisplay=" + isdisplay + ", cityid="
                + cityid + ", name=" + name + ", addr=" + addr + "]";
    }
    
    
}
